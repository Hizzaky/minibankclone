                            <h5>Pengertian akad mudharabah dalam perbankan syariah</h5>
                            <p>Dalam aspek perbankan syariah, akad mudharabah adalah jenis akad yang cukup banyak ditemukan di berbagai jenis produk maupun program yang ditawarkan oleh bank syariah. Berdasarkan pengertian yang dirilis oleh Otoritas Jasa Keuangan (OJK), salah satu produk bank syariah yang memiliki ketentuan operasional menggunakan akad mhudharabah adalah pembiayaan. Hal ini ditekankan berdasarkan prinsip bank syariah secara umum.</p>
                            <p>penting bagi pihak bank selaku penyedia modal menyalurkan pembiayaan serta bagi hasil berdasarkan akad mudharabah dan akad lain yang tidak bertentangan dengan syariah Islam dalam menjalankan kegiatan usaha perbankan. Dalam undang-undang nomor 21 tahun 2008 tentang Perbankan Syariah juga dijelaskan bahwa kerugian dalam perjanjian yang sidang berlansung nantinya akan ditanggung sepenuhnya oleh bank syariah, kecuali jika pihak kedua melakukan kesalahan yang disengaja, lalai atau menyalahi perjanjian atau detail dari akad mudharabah yang tilah disetujui. dengan kata lain, akad mudharabah adalah bentuk kerja sama yang mendapat dukungan penuh daru hukum di indonesia</p>
                            <p>Dalam pengertian yang diterbitkan oleh OJK, akad mydharabah adalah akd yang  bisa digunakan untuk kegiatan penghimpunan dana dalam bentuk investasi syariah. Investasi syariah yang dimaksud hadir dalam bentuk deposito, tabungan, atau bentuk produk perbankan lainnya</p>

                            <h5>Konsep Mudharabah</h5>
                            <p>Seiring perkembangan zaman, ketentuan dari akad mudharabah pun juga mengalami inovasi dari masa ke masa. jika membicarakan mengenai konsep mudharabah klaisk, akad mudharabah adalah sebuah perjanjian yang hanya dilakukan dengan satu jenis atau bentuk kerja sama dan tidak bisa digabungkan dengan akad jenis lainnya. namun, saat ini konsep akad mudharabah jadi memiliki fleksibilitas untuk dapat digabungkan dengan akad lain dalam seperti akad mudharabah atau musyarakah dalam sebuah aktivitas perbankan sayriah. penggabungan masyarakat untuk mendaapatkan layanan jasa perbankan syariah yang lebih baik.</p>
                            <p>Tidak hanya mengacu pada jenis kerja samanya saja, perkembangan konsep mudharabah ini juga mempengaruhi mekanisme pembayaran atau angsuran yang dilakukan oleh pihak kedua terkait manfaat pembiayaan yang diterima. Dalam konsep mudharabah klasik, dijelaskan bahwa praktik mekanisme angsuran dalam pembayaran modal pokok tersebut hanya dilakukan satu kali di akhir periode kontrak. Hal ini juga berlaku untuk mekanisme pembayaran bagi hasil mudharabah adalah dilakukan satu kali di akhir periode kontrak.</p>
                            <p>Untuk aktivitas perbankan syariah menggunakan akad mudharabah yang beroperasi di Indonesia saat ini, ketentuan aktivitasnya diatur melalui fatwa-fatwa yang dikeluarkan</p>
                            <p>oleh DSN - MUI, serta Peraturan OJK. Merujuk pada fatwa DSN - MUI Nomor: 07/DSN/MUI/IV/2000 mengenai pembiayaan mudharabah, dijelaskan bahwa akad mudharabah adalah akad atau perjanjian kerja sama suatu usaha antara dua pihak. Kedua pihak yang dimaksud adalah pemilik modal yang menyediakan seluruh modal sebagai pihak pertama dan pengelola modal yang bertindak sebagai penerima dan pengelola modal yang diberikan sebagai pihak kedua. </p>
                            <p>Dalam fatwa tersebut juga dijelaskan mengenai jangka waktu dari kerja sama akad mudharabah. Jangka waktu kerja sama akad mudharabah adalah ditentukan berdasarkan kesepakatan kedua belah pihak. Dengan kata lain akad mudharabah adalah salah satu akad yang menawarkan kemudahan serta fleksibilitas untuk bisa mengakomodasi kebutuhan serta keuntungan manfaat yang diterima oleh kedua belah pihak. </p>
                            <p>Selain itu, sebagai pihak pertama, pengelola modal juga diperbolehkan untuk menentukan jenis usaha apa yang akan dikembangkan berdasarkan kesepakatan akad mudharabah yang disepakati bersama dan sesuai dengan aturan syariah. Meskipun pemilik modal diperbolehkan untuk menentukan jenis usaha apa yang akan dikembangkan, dalam akad yang disepakati, pihak pertama tidak boleh ikut dalam manajemen jenis usaha dalam usaha tersebut. </p>
                            <p>Pihak pertama selaku pemilik modal dalam kesepakatan akad mudharabah adalah pihak yang mempunyai hak dan juga peran untuk melakukan pengawasan dan pembinaan terkait usaha yang telah disepakati tersebut. Hal ini dilakukan demi meminimalisasi risiko terhadap jenis usaha yang dilakukan di masa yang akan datang. Pasalnya, akad mudharabah adalah akad yang memiliki prinsip pembiayaan tanpa jaminan pasti. Jaminan tersebut hanya bisa diperoleh berdasarkan kesepakatan di antara kedua belah pihak. Dengan kata lain, transparansi merupakan salah satu kunci utama akad mudharabah ini bisa dilakukan.</p>

