                            <h5>Apa Itu Ijarah?</h5>
                            <p>Ijarah berasal dari bahasa Arab yang memiliki makna imbalan, atau upah sewa/jasa. Istilah “Ijarah” pada umumnya digunakan dalam perbankan syariah. Secara makna dan konteksnya dalam perbankan, Ijarah adalah pemindahan hak guna suatu barang dengan pembayaran biaya sewa tanpa diikuti pemindahan kepemilikan atas barang tersebut. Singkat kata Ijarah berarti menyewa suatu tanpa maksud memilikinya.</p>
                            <p>Lebih lanjut, yang berperan sebagai penyewa adalah nasabah dengan objek yang akan disewakan dan bank adalah pihak yang menyewakan. Transaksi dengan akad Ijarah diatur dalam Fatwa MUI tentang Pembiayaan Ijarah Nomor 09/DSN-MUI/VI/2000. Oleh sebab itu, pembiayaan dengan akad Ijarah diatur sesuai syariat Islam.</p>
                            <p>Baik proses maupun Imbalan dari transaksi Ijarah ini sendiri juga berdasarkan hasil kesepakatan kedua belah pihak. Bukan hanya itu saja, tujuan dari penyewaan barang atau asset tersebut haruslah jelas dan telah diketahui sebelumnya. Akad Ijarah berfokus kepada manfaat barang dan tidak boleh dilakukan atas suatu benda. Misalkan saja apabila ada seekor sapi yang diIjarahkan untuk diambil susunya, hal ini tidak diperbolehkan karena susu dapat menjadi benda yang dapat diperjual-belikan.</p>

                            <h5>Syarat Ijarah</h5>
                            <ol>
                                <li>Kedua pihak yang melakukan transaksi Ijarah sudah dewasa (baligh) dan berakal (tidak mabuk).</li>
                                <li>Kedua pihak yang melakukan transaksi memiliki kerelaan dan tidak didasarkan suatu paksaan dari pihak mana pun.</li>
                                <li>Barang yang menjadi objek transaksi harus jelas adanya.</li>
                                <li>Barang yang menjadi objek transaksi harus halal sesuai syariat Islam.</li>
                                <li>Barang yang menjadi objek transaksi menjadi hak Mu’jar atas seizin pemiliknya.</li>
